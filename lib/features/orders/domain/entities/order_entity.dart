class Order {
  final String id;
  final String name;
  final String description;
  final DateTime date;
  final String from;
  final String to;
  final String status;
  final double price;

  Order({
    required this.id,
    required this.name,
    required this.description,
    required this.date,
    required this.from,
    required this.to,
    required this.status,
    required this.price,
  });

  
}
